package com.star;

/**
 * 参数
 *
 * @author: star.zhu
 * @date: 2020-06-22
 */
public class LogParameters {

    /**
     * 参数值
     */
    public String value;

    /**
     * 参数类型
     */
    public String type;

    public String getType() {
        return type;
    }

    public String getValue() {
        return value;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
